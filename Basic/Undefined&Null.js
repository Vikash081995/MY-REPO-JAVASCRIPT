//`null` is a language keyword that evaluates to a special value that is usually used to indicate the absence of a value

// `undefined` value represents a deeper kind of absence.

// ==========================================
//                 Undefined
// =========================================
let fName;
console.log(typeof fName); //undefined

// ==========================================
//                 null
// =========================================
let myVariable = null;
console.log(myVariable);//null


